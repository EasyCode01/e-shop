import mongoose, { Schema } from "mongoose";

const productSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
      min: 0,
    },
    category: {
      type: String,
      required: true,
    },
    stock: {
      type: Number,
      required: true,
      min: 0,
    },
    images: [String],
  },
  { timestamps: true }
);

export const Product =
  mongoose.models?.Product || mongoose.model("Product", productSchema);

console.log("existing models:", mongoose.models.Product);
